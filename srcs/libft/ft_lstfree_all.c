#include "libft.h"

void	ft_lstfree_all(t_list **lst)
{
	t_list	*ptr;

	ptr = *lst;
	if (lst)
	{
		while (*lst)
		{
			ptr = (*lst)->next;
			free((*lst));
			(*lst) = ptr;
		}
	}
}
